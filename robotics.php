   <h3> Applications:</h3>

<li>As robots are designed for specific tasks this method of classification
 becomes more relevant. For example, many robots are designed for assembly work,
 which may not be readily adaptable for other applications. They are termed as "assembly robots".
 For seam welding, some suppliers provide complete welding systems with the robot i.e. the welding 
equipment along with other material handling facilities like turntables etc. as an integrated unit.
 Such an integrated robotic system is called a "welding robot" even though its discrete manipulator 
unit could be adapted to a variety of tasks. Some robots are specifically designed for heavy load 
manipulation, and are labelled as "heavy duty robots".</li>





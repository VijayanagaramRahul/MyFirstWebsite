<h3 align="left"><strong>What is Cloud Computing?</strong></h3>
<li>It is the most important and trending part of every engineering students life. It is playing the biggest role than IOT, BigData, etc. It is a type of Internet-based computing where different services including servers, storage, and applications are delivered to an organization's computers and devices through.</li>
<li>It is a central remote service to maintain data and applications. In business, it plays the main role to use applications without installation and access their personal files from any computer via internet access.</li>
 <li>  The main benefit of cloud computing is hardware failure do not result in data loss because of networked backups. Cloud computing used remote resources, saving organizations the cost of servers. That is a "utility" part is followed means users only pay for the resources they use.</li>
 <li> Finally, for the storing data, it helps a lot for everyone not only for engineering students. There are highest paid jobs in India for working on cloud computing.</li> </h3>
<h3> Characteristics of cloud computing:</h3>
<strong>Broad network access:</strong>
<li>Capabilities are available over the network and accessed through standard mechanisms that promote use by heterogeneous thin or thick client platforms (e.g., mobile phones, tablets, laptops, and workstations).</li>
<strong>On-demand self-service: </strong>
<li> A consumer can unilaterally provision computing capabilities, 
such as server time and network storage, as needed automatically without requiring 
human interaction with each service provider.</li>
<strong>Measured service: </strong>
 <li> Cloud systems automatically control and optimize resource use
 by leveraging a metering capability at some level of abstraction appropriate to the 
type of service (e.g., storage, processing, bandwidth and active user accounts).
 Resource usage can be monitored, controlled and reported, providing transparency 
for the provider and consumer.
SP 800-145 also defines four deployment models - private, community, public and
 hybrid - that together categorize ways to deliver cloud services.</li>
<strong>Resource pooling: </strong>
<li>The provider's computing resources are pooled to serve multiple
 consumers using a multi-tenant model, with different physical and virtual resources
 dynamically assigned and reassigned according to consumer demand. There is a sense 
of location independence in that the customer generally has no control or knowledge
 over the exact location of the provided resources but may be able to specify location
 at a higher level of abstraction (e.g., country, state or datacenter). Examples of resources 
include storage, processing, memory and network bandwidth.</li>
<strong>Rapid elasticity:</strong>
<li>Capabilities can be elastically provisioned and released, in some cases 
automatically, to scale rapidly outward and inward commensurate with demand. 
To the consumer, the capabilities available for provisioning often appear to be 
unlimited and can be appropriated in any quantity at any time.</li>
<strong>Different types of cloud Computing:</strong>
<li>Software as a Service:: It involves the licensure of a software application to customers. Licenses are typically provided through a pay as you go model or on-demand. </li>
<li>Platform as a Service:: It is considered the most complex. It shares some similarities with Software as a service, the primary difference being that instead of delivering software online, it is actually a platform for creating software that is delivered via the internet. </li>
<li>Infrastructure as a Service:: It involves a method for delivering everything from operating systems to servers and storage through IP-based connectivity as part of an on-demand service.</li>

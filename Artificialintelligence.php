  <h3 align="left"> What is AI?</h3>
  <li>
First of all, AI stands for ARTIFICIAL INTELLIGENCE. In the field of computer science, artificial intelligence also called machine intelligence. It is the intelligence demonstrated by machines, in contrast to the natural intelligence displayed by humans and other animals.</li>

<h3 align="left">What is the need of AI in computer science?</h3>
<li>AI is an area of study in computer science that focuses on creating computer systems that perform tasks that require human intelligence. This can include visual perception, decision making, translation, and even speech recognition.</li>

<li>The overall goal of Artificial Intelligence is to create technology that allows computers and machines to function in an intelligent manner. Artificial Intelligence in the machines which are designed and programmed in such a way that they think and act like a human. Artificial Intelligence becomes an important part of our daily life. Our life is changed by Artificial Intelligence because this technology is used in a wide area of day to day services.</li> 
<h3><strong>why AI?</strong></h3>
<li>AI gets the most out of data. When algorithms are self-learning, the data itself can become intellectual property. The answers are in the data; you just have to apply AI to get them out. Since the role of the data is now more important than ever before, it can create a competitive advantage. If you have the best data in a competitive industry, even if everyone is applying similar techniques, the best data will win.</li>
<li>AI adds intelligence to existing products. In most cases, AI will not be sold as an individual application. Rather, products you already use will be improved with AI capabilities, much like Siri was added as a feature to a new generation of Apple products. Automation, conversational platforms, bots, and smart machines can be combined with large amounts of data to improve many technologies at home and in the workplace, from security intelligence to investment analysis.</li>


    <h3 align="center">BLOCK CHAIN</h3>
    
    <h3>What is Blockchain?</h3>
    <li>A blockchain is a public ledger of information collected through a network that sits on top of the internet. It is how this information is recorded that gives blockchain its groundbreaking potential.</li>
    <li>The technology can be used to develop blockchain applications, such as social networks, messengers, games, exchanges, storage platforms, voting systems, prediction markets, online shops and much more.</li>
    <h3>Types of Blockchain:</h3>
    There are 3 types of blockchain technologies,they are<br>
    <li>1)Public blockchains</li>
    <li>2)Private blockchains</li>
    <li>3)Consortium blockchains</li>
    <h3>Public blockchains:</h3>
    <li>State of the art public Blockchain protocols based on Proof of Work (PoW) consensus algorithms are open source and not permission. Anyone can participate, without permission. Anyone can download the code and start running a public node on their local device, validating transactions in the network, thus participating in the consensus process â€“ the process for determining what blocks get added to the chain and what the current state is. Anyone in the world can send transactions through the network and expect to see them included in the blockchain if they are valid. Anyone can read transaction on the public block explorer.</li>
    <h3>Private blockchains:</h3>
    <li>Write permissions are kept centralized to one organization. Read permissions may be public or restricted to an arbitrary extent. Example applications include database management, auditing, etc. which are internal to a single company, and so public readability may in many cases not be necessary at all. In other cases public audit ability is desired. Private blockchains are a way of taking advantage of blockchain technology by setting up groups and participants who can verify transactions internally. This puts you at the risk of security breaches just like in a centralized system, as opposed to public blockchain secured by game theoretic incentive mechanisms. However, private blockchains have their use case, especially when it comes to scalability and state compliance of data privacy rules and other regulatory issues.</li>
    <h3>Consortium blockchains:</h3>
    <li>Consortium blockchains differ to their public counterpart in that they are permissioned, thus, not just anyone with an internet connection could gain access to a consortium blockchain. These types of blockchains could also be described as being semi-decentralized. Control over a consortium blockchain is not granted to a single entity, but rather a group of approved individuals. With a consortium blockchain, the consensus process is likely to differ to that of a public blockchain. Instead of anyone being able to partake in the procedure, consensus participants of a consortium blockchain are likely to be a group of pre-approved nodes on the network.  Thus, consortium blockchains possess the security features that are inherent in public blockchains, whilst also allowing for a greater degree of control over the network.</li>
    <h3>Application of blockchain:</h3>
    <li>Financial services</li>
    <li>Healthcare</li>
    <li>Identity</li>
    <li>Supply chain</li>
    <li>Real estates</li>
    
    

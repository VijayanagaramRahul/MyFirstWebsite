    <h3 align="center">IOT</h3>
    <p><h3>What is IOT?</h3><br>
    <li>IOT stands for 'Internet Of Things'. It is nothing but enlarging the power of the internet beyond computers and smartphones to a whole range of other things, processes, and environments. Those connected things are used to gather information and send the information back</li><br>
    <h3>Why IOT?</h3><br>
    <strong>1. Environmental Monitoring</strong>
<p>let us see an example: You surely noticed that while you surf the net and suddenly when you open your Gmail you see some interesting thing which is near to your favorite. Or in facebook when you like the page, on the right section similar content page will be appeared. These are common and tangential but image when we can monitor all embedded computing system to improve our life. Like we can monitor wildlife habit and by this tracking prepare them their desire condition and prevent their extinction.</p><br>
<strong>2. Infrastructure Management</strong>
<p>Infrastructure Management is useful for monitoring and tracking if there is any problem in urban or rural Infrastructure such as bridge, railway, etc to reduce risk of danger and failure in strength would be tested and generate an alert as soon as possible to repair it.</p><br>
<strong>3. Building and Home Automation</strong>
<p>It is related to everything in the home which have the potential to monitor and remote control such as air condition, security lock lightening, heating, ventilation, telephone system, tv to make comfort, secure, with low energy consumption.</p><br>
<strong>4. Transport Systems</strong>
<p>Transport Systems makes regular city and environment without less employer for police or station such as an automatic configuration in traffic lights, smart parking, traffic camera to detect which road has heavy traffic and automatically offer less crowd road, or smart camera which fine driver in high speed.</p><br>
<strong>5. Industrial Applications</strong>
<p>Industrial Applications investigate the quality of the product in order to real-time optimizing to have good marketing such as who is most interested in which product and how this product can find marketing with which tiny changes.</p><br>
<strong>6. Medical and Healthcare Systems</strong>
<p>Healthcare Systems helps to improve patient state better by monitoring and controlling their heart rate or blood pressure or even for their diet. A smart tablet which shows us how much dows with which gradient can help the patient to get better.</p>


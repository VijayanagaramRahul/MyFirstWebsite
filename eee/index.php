      <div class="container" >


<p>An electrical engineer is someone who designs and develops new electrical equipment, solves problems and tests equipment. They work with all kinds of electronic devices, from the smallest pocket devices to large supercomputers. Electrical engineering deals with electricity, and electronics. An electrical engineer is someone who designs and develops new electrical systems, solves problems and tests equipment. They study and apply the physics and mathematics of electricity, electromagnetism, and electronics to both large and small scale systems to process information and transmit energy.</p>
<p> One of the most major advantages is there Wide Range of Career Opportunities.
 Electrical engineers can choose to work in a variety of industries including 
telecommunications, computer, aerospace, and manufacturing industries. 
They can progress their career even further by specializing in specific areas.
Electrical engineering is rapidly growing not only within core areas such as wireless communications and mobile technologies but also in other cross-functioning areas including nanotechnology and biomedical engineering.
The world is moving towards the renewable source of energy including hydel, solar and wind power, and multi-national corporations are searching for power generation experts.
</p>
<strong>Sub fields in EEE are:</strong>
<br><strong>1)Power: </strong>
<p>It deals with the transmission, generation as well as the design of a range of related devices These include transformers, electric generators, electric motors, high voltage engineering, and power electronics. In many regions of the world, governments maintain an electrical network called a power grid that connects a variety of generators together with users of their energy.</p>
<strong>2)control: </strong><p>
    
Control engineering focuses on the modeling of a diverse range of dynamic systems and the design of controllers that will cause these systems to behave in the desired manner. To implement such controllers, electrical engineers may use electronic circuits, digital signal processors, microcontrollers, and programmable logic controllers.</p>

<strong>3)Electronics:</strong><p>
    Electronic engineering involves the design and testing of electronic circuits that use the properties of components such as resistors, capacitors, inductors, diodes, and transistors to achieve a particular functionality.</p>
<strong>4)Computers:</strong>
<p>Computer engineering deals with the design of computers and computer systems. This may involve the design of hardware, PDAs, tablets, and supercomputers, or the use of computers to control an industrial plant.</p>
<strong>5)Instrumentation:</strong><p>Instrumentation engineering deals with the design of devices to measure physical quantities such as pressure, flow, and temperature.</p>
<strong>6)Telecommunication:</strong><p>
     Telecommunication focuses on the transmission of information across a communication channel such as an optical fiber. 
</p>

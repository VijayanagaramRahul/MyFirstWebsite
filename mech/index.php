      <h2 class="heading" style="top:-10px; color:#005580;font-family:Cursive;">MECHANICAL</h2>


<p style="font-family:;"><li>Mechanical engineers are involved with the design, analysis, testing, manufacturing, control, operation, and maintenance of mechanical systems. </li>
<li>Mechanical Engineering is Important, It deals with all aspects of the conversion of thermal energy into
 useful work and the machines that make this possible.</li>
 <li>Mechanical engineers design power-producing machines such as electric generators, internal combustion engines, steam and gas turbines as well as power-using machines, such as refrigeration and 
air-conditioning systems. </li><br>
Mechanical engineers design other machines inside buildings, such as elevators and escalator.
 Mechanical Engineers have good graduate prospects.<br>
 <li>In a notoriously tough graduate job market, it's fair to say that degree-level engineers have an edge.</li>
 <li>Engineers build societies and, mechanical engineers specifically, apply fundamental maths and physics 
laws to create and build mechanical devices we use every day.</li>
<p><strong>Mechatronics and Robotics:</strong>Mechatronics is a combination of mechanics and electronics. It is an interdisciplinary branch of mechanical engineering, electrical engineering and software engineering that is concerned with integrating electrical and mechanical engineering to create hybrid systems. <br></p>

<p>Robotics is the application of mechatronics to create robots, which are often used in industry to perform tasks that are dangerous, unpleasant, or repetitive. These robots may be of any shape and size, but all are preprogrammed and interact physically with the world. To create a robot, an engineer typically employs kinematics and mechanics </p>

<p>Robots are used extensively in industrial engineering. They allow businesses to save money on labor, perform tasks that are either too dangerous or too precise for humans to perform them economically, and to ensure better quality.</p>
<p><strong>Design and Drafting:</strong>Drafting or technical drawing is the means by which mechanical engineers design products and create instructions for manufacturing parts. A technical drawing can be a computer model or hand-drawn schematic showing all the dimensions necessary to manufacture a part, as well as assembly notes, a list of required materials, and other pertinent information.<br>
Instructions for manufacturing a part must be fed to the necessary machinery, either manually, through programmed instructions, or through the use of a computer-aided manufacturing (CAM) or combined CAD/CAM program. Optionally, an engineer may also manually manufacture a part using the technical drawings.</p>

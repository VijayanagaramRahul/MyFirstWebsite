    <h3 align="center">AUGMENTED REALITY</h3>
    <h3>What is Augmented Reality?</h3>
    <li>Augmented reality is the result of using technology to superimpose information  sounds, images, and text  on the world we see. Picture the "Minority Report" or "Iron Man" style of interactivity.</li>
    <h3>Why is Augmented Reality?</h3>
    <li>Augmented reality is providing new ways to deliver digital 3D models and data. And propose a specific use of computer-based 3D modeling for the design development process. Augmented Reality technology used to create virtual product models, addresses the inherent difficulties with the delivery of physical models and also facilitates sharing feedback with all constituents involved in the design process.</li>
    <h3>Types of Augmented Reality:</h3><br>
    There are 4 types of augmented reality, they are<br>
    <li>1)Marker Based Augmented Reality</li>
<li>2)Marker-less Augmented Reality</li>
<li>3)Projection Augmented Reality</li>
<li>4)Superimposition Based Augmented Reality</li>
<h3>1)Marker Based Augmented Reality:</h3>
<li>It is also called Image Recognition or Recognition based AR. In simple words, this technology uses the camera in AR device to produce a result. QR and 2D are the examples of the visual marker. The users get the result when camera reader senses the marker.</li>
<h3>2)Marker less Augmented Reality:</h3>
<li>It is also called location-based AR. It is one of the most widely used and implemented type of technology. Marker less is also known as position based AR and GPS based Augmented Technology.

Marker Less AR is the only type of AR technology which does not use any kind of recognition system and hence it does not require augmentation. Another difference is, the location-based AR does not know about the object whereas recognition based AR apps do. To provide a fecund result, this technology uses various location tools and helps in recognizing location on your devices.</li>
<h3>3)Projection Augmented Reality:</h3>
<li>It is based on AR projection function, where lights from the device projected on the objects.
There are numerous approaches which make this technology more interesting. One of the approaches is throwing the light onto the surface of the real world and makes it interactive by the human touch with the help of sensors. The projection can also be done in mid-air with the help of laser plasma technology. It helps in studying the structure and configurations of projected images.</li>
<h3>4)Superimposition Based Augmented Reality:</h3>
<li>It is indeed one of the most important types of technology where object recognition plays an important role. In this technology, the augmented image can replace the original image, either partially or fully. The technology is useful in the medical field. A doctor can thoroughly examine patient and gives a proper treatment.</li>
<h3>Advantages of Augmented Reality</h3>
<li>The AR system is highly interactive in nature and operates simultaneously with real time environment.</li>
<li>It can save money by testing critical situations in order to confirm their success without actually implementing in real time. Once it is proven, it can be implemented in real world.</li>
<li>It can be used by military people without putting their life in danger by way of battle field simulation before the actual war. This will also help them in actual war to take critical decisions.</li>
<li>Due to its use in medical industry, life of patients have become safer. It helps in efficient diagnosis of diseases and in early detection of them.</li>
    

